function getCascaderParentsId(list : any[], id : number) {
    for (const i in list) {
        // const obj = list[i]
        if (list[i].id === id) {
            return [list[i].id]
        }
        if (list[i].childrens) {
            const node: any = getCascaderParentsId(list[i].childrens, id)
            debugger
            if (node !== undefined) {
                node.unshift(list[i].id)
                return node
            }
        }
    }
}

